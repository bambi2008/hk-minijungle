import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const projectRoot = process.cwd();
const dataDir = path.join(projectRoot, "data");

function scriptSafe(value) {
  return value.replace(/</g, "\\u003c");
}

function inlineScriptSafe(value) {
  return value.replace(/<\/script/gi, "<\\/script");
}

const [htmlSource, cssSource, appSource, logoBuffer] = await Promise.all([
  readFile(path.join(projectRoot, "index.html"), "utf8"),
  readFile(path.join(projectRoot, "styles.css"), "utf8"),
  readFile(path.join(projectRoot, "app.js"), "utf8"),
  readFile(path.join(projectRoot, "assets", "dr-forest-logo.png"))
]);

const embeddedData = {};
for (const filename of await readdir(dataDir)) {
  if (!filename.endsWith(".json")) continue;
  embeddedData[`data/${filename}`] = JSON.parse(
    await readFile(path.join(dataDir, filename), "utf8")
  );
}

const logoDataUrl = `data:image/png;base64,${logoBuffer.toString("base64")}`;
const embeddedDataJson = scriptSafe(JSON.stringify(embeddedData));
const fetchShim = `
<script type="application/json" id="__dr-forest-data">${embeddedDataJson}</script>
<script>
(() => {
  const embedded = JSON.parse(document.getElementById("__dr-forest-data").textContent);
  const nativeFetch = window.fetch.bind(window);

  window.fetch = (input, init) => {
    const raw = typeof input === "string" ? input : input?.url || "";
    const url = new URL(raw, window.location.href);
    const basePath = window.location.pathname.replace(/[^/]*$/, "");
    const sameOriginPath = url.origin === window.location.origin && url.pathname.startsWith(basePath)
      ? url.pathname.slice(basePath.length)
      : url.pathname.replace(/^.*\\/minijungle-fm-ops\\//, "");
    const normalized = decodeURIComponent(sameOriginPath || raw).replace(/^\\.\\//, "").replace(/^\\//, "");

    if (Object.prototype.hasOwnProperty.call(embedded, normalized)) {
      return Promise.resolve(new Response(JSON.stringify(embedded[normalized]), {
        status: 200,
        headers: { "content-type": "application/json" }
      }));
    }

    return nativeFetch(input, init);
  };
})();
</script>`;

let publicHtml = htmlSource
  .replace("<title>DR FOREST FM Ops</title>", "<title>DR FOREST FM Ops Public Demo</title>")
  .replace('<link rel="icon" href="assets/dr-forest-logo.png">', `<link rel="icon" href="${logoDataUrl}">`)
  .replace('<link rel="stylesheet" href="styles.css">', `<style>\n${cssSource}\n</style>`)
  .replaceAll('src="assets/dr-forest-logo.png"', `src="${logoDataUrl}"`)
  .replace(
    '<script src="app.js"></script>',
    `${fetchShim}\n<script>\n${inlineScriptSafe(appSource)}\n</script>`
  );

publicHtml = `<!-- Generated by scripts/build-public-demo.mjs. Do not edit by hand. -->\n${publicHtml}`;

await writeFile(path.join(projectRoot, "public-demo.html"), publicHtml, "utf8");
console.log("Built public-demo.html");
